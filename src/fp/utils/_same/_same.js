/**
 * Returns the same argument that was passed to it.
 *
 * @function
 * @name _same
 * @param {*} arg - Any value.
 * @returns {*} The same value that was passed as an argument.
 * @description
 * This function is an identity function, returning exactly what was passed to it.
 */
export function _same(arg) {
    return arg;
}
